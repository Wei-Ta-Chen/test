//===========================================================================		
/*
  @brief		Enable Golbal Int.
              
*/
//===========================================================================
void	enable_golbal_int(void)
{
	INTCONbits.GIE_GIEH = 1;
	INTCONbits.PEIE_GIEL = 1;	
}

//===========================================================================		
/*
  @brief		Disable Golbal Int.
              
*/
//===========================================================================
void	disable_golbal_int(void)
{
	INTCONbits.GIE_GIEH = 0;
	INTCONbits.PEIE_GIEL = 0;	
}

//===========================================================================		
/*
  @brief		enable portb change interrupt
              
*/
//===========================================================================
void	enable_portb_change_interrupt(void)
{
	INTCONbits.RBIF = 0;
	INTCONbits.RBIE = 1;			
}

//===========================================================================		
/*
  @brief		disable_portb_change_interrupt
               
*/
//===========================================================================
void	disable_portb_change_interrupt(void)
{
	INTCONbits.RBIE = 0;
	INTCONbits.RBIF = 0;	
}

