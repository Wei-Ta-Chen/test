#include	"voice_test.h"

//===========================================================================		
/*
  @brief		register definition.                  
*/
//===========================================================================
uint8_t		volume_level;


//===========================================================================		
/*
  @brief		
  
  @details		
  
*/
//===========================================================================
void	volume_level_set(void)
{
	switch(volume_level)	
	{
//===========================================================================
		case	0:
		
		// -15dB.
		audio_db_set(5, 1, 0);
		
		break;		

//===========================================================================
		case	1:
		
		// -10dB.
		audio_db_set(0, 1, 0);
		
		break;	
		
//===========================================================================
		case	2:
		
		// -5dB.
		audio_db_set(5, 0, 0);
		
		break;	

//===========================================================================
		case	3:
		
		// 0dB.
		audio_db_set(0, 0, 0);
		
		break;

//===========================================================================
		case	4:
		
		// 5dB.
		audio_db_set(0, 0, 5);
		
		break;	

//===========================================================================
		case	5:
		
		// 10dB.
		audio_db_set(0, 0, 10);
		
		break;	

//===========================================================================
		case	6:
		
		// 15dB.
		audio_db_set(0, 0, 15);
		
		break;	

//===========================================================================
		default:	
		
		// 0dB.
		audio_db_set(0, 0, 0);								
		
//===========================================================================
	}	
}

//===========================================================================		
/*
  @brief		
  
  @details		
  
*/
//===========================================================================
void	volume_level_inc(void)
{
	volume_level++;
	
	if(volume_level > VOLUME_LEVEL_MAX)	
	{
		volume_level = 0;
	}		
}

//===========================================================================		
/*
  @brief		
  
  @details		
  
*/
//===========================================================================
void	volume_level_dec(void)
{		
	if(volume_level > VOLUME_LEVEL_MIN)	
	{
		volume_level--;
	}		
}